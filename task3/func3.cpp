#include <iostream>

// Функция, генерирующая исключение типа double
void func6(){
    throw 2.71;  
}

// Функция, выполняющая вычисления и вызывающая func6()
void func5(){
    int res;
    res = 1+2; 
    std::cout << "Функция 5: Результат 1 + 2 = " << res << std::endl;
    func6();  // Вызываем функцию, которая сгенерирует исключение
}

// Функция, пытающаяся обработать исключение типа int
void func4(){
    try {
        func5();  // Вызываем функцию, которая может сгенерировать исключение
    } catch (int e) {
        // Этот блок выполнится, если будет поймано исключение типа int
        std::cout << "Функция 4: Поймано исключение типа int: " << e << std::endl;
    }
   
}

// Функция с обработчиками для исключений int и double
void func3(){
    try {
        func4();  // Вызываем функцию, которая может сгенерировать исключение
    } catch (int e) {
        // Обработчик для исключения типа int
        std::cout << "Функция 3: Поймано исключение типа int: " << e << std::endl;
    } catch (double e) {
        // Обработчик для исключения типа double
        std::cout << "Функция 3: Поймано исключение типа double: " << e << std::endl;
    }
 
}

// Функция, выполняющая вычисления и вызывающая func3()
void func2(){
    int res;
    res = 1+2;  
    std::cout << "Функция 2: Результат 1 + 2 = " << res << std::endl;
    func3();  // Вызываем функцию с обработкой исключений
}
